#Декоратор, который будет запускать функцию указанное число раз с указанными параметрами и возвращать последвательность результатов.

# 1. ДЕКОРАТОР С ПАРАМЕТРАМИ (запускает функцию N раз и возвращает список результатов)
def repeat(n):                           # Фабрика декораторов — принимает число повторений
    def decorator(func):                 # Сам декоратор — принимает функцию (замыкание)
        def wrapper(*args):              # Обёртка — принимает любые аргументы
            results = []                 # Список для накопления результатов
            for _ in range(n):           # Цикл n раз
                res = func(*args)        # Вызываем оригинальную функцию с аргументами
                results.append(res)      # Добавляем результат в список
            return results               # Возвращаем список всех результатов
        return wrapper                   # Возвращаем обёртку
    return decorator                     # Возвращаем готовый декоратор

# 2. ФАБРИКА ЗАМЫКАНИЙ (создаёт калькулятор с накоплением)
def make_calc(op, initial):              # op = "*", initial = 1
    result = initial                     # Переменная, которую "захватит" замыкание
    
    def calc(num):                       # САМО ЗАМЫКАНИЕ — видит result и op снаружи
        nonlocal result                  # Говорим: result не локальная, а из make_calc
        if op == "*":   result *= num
        elif op == "+": result += num
        elif op == "-": result -= num
        elif op == "/": result /= num
        return result                    # Возвращаем накопленный результат
    
    return calc                          # Возвращаем замыкание

# 3. ПРИМЕНЯЕМ ДЕКОРАТОР К ЗАМЫКАНИЮ
calc = make_calc("*", 1)                 # Создаём обычный калькулятор
calc = repeat(5)(calc)                   # Декорируем: теперь будет повторять 5 раз

# ===== ПРОВЕРКА =====
print(calc(2))                           # 5 раз умножаем на 2 с накоплением